include Camlboy_lib
open Uints

let area0_start_addr = Uint16.of_int 0x9800
let area1_start_addr = Uint16.of_int 0x9C00

let create () =
  Tile_map.create
    ~tile_map_ram:(Ram.create ~start_addr:area0_start_addr ~end_addr:(Uint16.of_int 0x9FFF))
    ~area0_start_addr
    ~area1_start_addr

let print_all_tiles ~area t =
  for y = 0 to 31 do
    for x = 0 to 31 do
      Tile_map.get_tile_id t ~area ~y ~x
      |> Int8.to_int
      |> Printf.printf "%d "
    done;
    Printf.printf "\n"
  done

